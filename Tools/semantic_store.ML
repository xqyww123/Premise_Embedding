(* Build (entity, name, prop_str, line, uk, prompt_hint, provenance,
   constituents) entries
   from (entity, name, pos, uk) tuples.  prompt_hint is extra context shown to
   the interpretation agent only (NOT stored as expr): the first members of a
   named_theorems collection, or the provenance of a fact generated by a locale
   interpretation.  The trailing provenance field (template_uk, locale_uk,
   qualifier; NONE for ordinary entries) is carried into the stored record.
   ONLY this function (build_entries) is shared by interpret' and
   interpret_entities.  build_entries itself renders a member's coll(i) from its
   member_idx; the Part C (entity_kind_int, name) uniqueness assert below runs on
   the live interpret' path ONLY — interpret_entities (currently dead) passes
   member_idx = NONE for every entity, so it never produces members; if it is
   revived with rule/member entities, factor Part C out and apply it there too. *)
fun build_entries (context : Context.generic)
    (raw : (Universal_Key.entity * string * Position.T * Universal_Key.universal_key * int option) list)
    : (Universal_Key.entity * string * string * int * Universal_Key.universal_key * string
       * (Universal_Key.universal_key option * Universal_Key.universal_key option * string) option
       * Universal_Key.constituent list) list =
  let
    val thy = Context.theory_of context
    val ctxt = Context.cases Proof_Context.init_global I context
    val pp = string_of_term ctxt
    val pp_typ = string_of_typ ctxt
    fun source_at_pos pos =
      case PIDE_State.command_at_position pos of
        SOME (src, _, _) => strip_trailing_begin src
      | NONE => ""
    fun mk_prop_str (entity, pos) =
      case entity of
        Universal_Key.Theorem thm =>
          (pp (Thm.prop_of thm)
           handle ERROR msg =>
             (warning ("mk_prop_str: Theorem pp failed: " ^ msg); raise ERROR msg))
      | Universal_Key.Introduction_Rule thm =>
          (pp (Thm.prop_of thm)
           handle ERROR msg =>
             (warning ("mk_prop_str: Introduction_Rule pp failed: " ^ msg); raise ERROR msg))
      | Universal_Key.Elimination_Rule thm =>
          (pp (Thm.prop_of thm)
           handle ERROR msg =>
             (warning ("mk_prop_str: Elimination_Rule pp failed: " ^ msg); raise ERROR msg))
      | Universal_Key.Induction_Rule thm =>
          (pp (Thm.prop_of thm)
           handle ERROR msg =>
             (warning ("mk_prop_str: Induction_Rule pp failed: " ^ msg); raise ERROR msg))
      | Universal_Key.Case_Split_Rule thm =>
          (pp (Thm.prop_of thm)
           handle ERROR msg =>
             (warning ("mk_prop_str: Case_Split_Rule pp failed: " ^ msg); raise ERROR msg))
      | Universal_Key.Constant cname =>
          (let val T = Consts.the_constraint (Sign.consts_of thy) cname
           in pp_typ T end
           handle TYPE _ => "" | ERROR _ => "")
      | Universal_Key.Type _ => source_at_pos pos
      | Universal_Key.Class _ => source_at_pos pos
      | Universal_Key.Locale _ => source_at_pos pos
      | Universal_Key.Theorem_Collection _ => source_at_pos pos
      | Universal_Key.Method _ => source_at_pos pos
      | _ => ""
    (* Extra prompt-only context (never stored as expr).  For a theorem
       collection, show up to 10 current member propositions so the agent can
       describe the collection's purpose; members are deliberately kept out of
       expr/key. *)
    fun mk_prompt_hint (Universal_Key.Theorem_Collection nt_name, _) =
          (case \<^try>\<open>Context_Callbacks.dynamic_fact_members (Context.Proof ctxt)
                          (Proof_Context.facts_of ctxt) nt_name
                  catch exn =>
                    (warning ("build_entries: theorem-collection member fetch failed for "
                      ^ quote nt_name ^ ", member preview omitted: " ^ Runtime.exn_message exn);
                     NONE)\<close> of
             NONE => ""
           | SOME [] => ""
           | SOME thms =>
               let
                 val shown = Library.take 10 thms
                 fun line thm = "  - " ^ (pp (Thm.prop_of thm) handle ERROR _ => "?")
                 val more = if length thms > 10
                            then ["  \226\128\166 (" ^ string_of_int (length thms - 10) ^ " more)"]
                            else []
               in
                 "Current members (context only \226\128\148 do NOT list them in your answer):\n"
                 ^ cat_lines (map line shown @ more)
               end)
      | mk_prompt_hint _ = ""

    (* --- locale-interpretation provenance (theory contexts only).
       Detection and key computation are pure and expected to succeed;
       genuine errors propagate (the pipeline is crash-and-resume). --- *)
    val provss =
      (case context of
         Context.Theory thy' =>
           let
             val pfacts = Context.cases Global_Theory.facts_of Proof_Context.facts_of context
             (* members carry the BARE coll name in raw (coll(i) is rendered only at
                the record-write below), so this both keeps detect's name tokens
                clean AND lets the is_dynamic bypass still fire for members. *)
             val detected =
               Locale_Instance.detect thy' (map (fn (_, name, _, uk, _) => (name, uk)) raw)
           in
             (* A dynamic collection itself (Theorem_Collection, name = the bin) and
                its members (Theorem, name = the bare bin) are not locale instances:
                bypass detection so they never carry provenance.  Keyed on the bare
                name being a dynamic fact — covers BOTH (member_idx alone would miss
                the collection entity).  The per-entry name is read post-dedup, so a
                surviving static entry keeps its own (non-dynamic) name and provenance. *)
             map2 (fn (_, name, _, _, _) => fn d =>
                     if Facts.is_dynamic pfacts name then [] else d)
                  raw detected
           end
       | Context.Proof _ => map (K []) raw)

    fun template_uk_of (p : Locale_Instance.provenance) =
      Option.map (Universal_Key.key_of_theorem' NONE context) (#template_thm p)
    val prim_provenance = provss |> map (fn [] => NONE
      | p :: _ => SOME (template_uk_of p,
                        SOME (#1 (Universal_Key.key_of_locale context (#locale_name p))),
                        #qualifier p))

    (* Batch-check which template/locale interpretations exist in the store.
       The RPC host is an environmental dependency (absent in standalone /
       test invocations of build_entries), so RPC failures only degrade the
       hint to name + proposition — but never silently. *)
    val wanted_uks =
      prim_provenance |> maps (fn NONE => [] | SOME (t, l, _) => map_filter I [t, l])
      |> distinct (op =)
    val available : unit Bytehashtab.table = Bytehashtab.empty 64
    val _ =
      if null wanted_uks then ()
      else \<^try>\<open>
        let
          val flags = Remote_Procedure_Calling.call_command contains_cmd wanted_uks
          val _ =
            length flags = length wanted_uks orelse
              error (String.concat ["Semantic_Store.contains protocol error: sent ",
                string_of_int (length wanted_uks), " keys, got ",
                string_of_int (length flags), " flags"])
        in
          ListPair.appEq
            (fn (uk, true) => Bytehashtab.update available (uk, ()) | _ => ())
            (wanted_uks, flags)
        end
      catch exn =>
        warning ("build_entries: provenance availability check failed, " ^
          "hints degrade to name + proposition: " ^ Runtime.exn_message exn)\<close>
    (* Stored interpretations are free-form agent prose and may carry embedded
       newlines.  Such a newline would split a single provenance segment (head /
       Locale "..." line) across several physical lines, breaking the per-line
       collapse on the Python side (semantic_interpretation.format_entries).
       Scan first (cheap, early-exit, no allocation) and only rebuild when a
       newline is actually present — the common case is newline-free. *)
    fun single_line s =
      let
        val n = size s
        val i = Unsynchronized.ref 0
        val has_nl = Unsynchronized.ref false
      in
        while !i < n andalso not (!has_nl) do
          (if String.sub (s, !i) = #"\n" then has_nl := true else ();
           i := !i + 1);
        if !has_nl
        then String.map (fn c => if c = #"\n" then #" " else c) s
        else s
      end
    val sem_cache : string option Bytehashtab.table = Bytehashtab.empty 64
    fun sem_of NONE = NONE
      | sem_of (SOME uk) =
          if not (Bytehashtab.defined available uk) then NONE
          else (case Bytehashtab.lookup sem_cache uk of
                  SOME r => r
                | NONE =>
                    let
                      val r =
                        \<^try>\<open>query_semantics_by_key uk false
                          catch exn =>
                            (warning ("build_entries: stored interpretation lookup failed: " ^
                               Runtime.exn_message exn); NONE)\<close>
                        |> Option.map single_line
                      val _ = Bytehashtab.update sem_cache (uk, r)
                    in r end)

    fun mk_instance_hint [] _ = ""
      | mk_instance_hint ((p : Locale_Instance.provenance) :: others) (tuk, luk) =
          let
            val params_str = #inst_params p
              |> map (fn (n, t) =>
                   n ^ " := " ^ pp t ^ " :: " ^ pp_typ (Term.fastype_of t))
              |> commas
            val head = String.concat
              ["Generated by a locale interpretation (the command at this entry's line): ",
               "instance of locale ", quote (#locale_name p),
               if #qualifier p = "" then "" else ", qualifier " ^ quote (#qualifier p),
               if null (#inst_params p) then "" else ", instantiating " ^ params_str,
               "."]
            val tmpl_id = #template_name p ^
              (if #template_index p = 0 then ""
               else "(" ^ string_of_int (#template_index p) ^ ")")
            val tmpl_line =
              (case #template_thm p of
                 SOME th =>
                   "Template " ^ tmpl_id ^ ": " ^
                   (pp (Thm.prop_of th) handle ERROR _ => "?")
               | NONE => "Template " ^ tmpl_id)
            val tmpl_sem = sem_of tuk |> Option.map (fn s => "Template meaning: " ^ s)
            val loc_sem = sem_of luk
              |> Option.map (fn s => "Locale " ^ quote (#locale_name p) ^ ": " ^ s)
            val collapsed =
              (case others of
                 [] => NONE
               | _ => SOME ("Under this instantiation the same proposition also arises from: "
                   ^ commas (distinct (op =) (map #template_name others))))
          in
            cat_lines (map_filter I
              [SOME head, SOME tmpl_line, tmpl_sem, loc_sem, collapsed,
               SOME "(Provenance shown as context only \226\128\148 translate the fact itself.)"])
          end

    fun is_thm_entity (Universal_Key.Theorem _) = true
      | is_thm_entity (Universal_Key.Named_Theorem _) = true
      | is_thm_entity _ = false

    (* Constituent theories of theorem/rule entities (stored in the record for
       deletion/migration by theory).  thm-carrying entities recompute through
       the digest-level cache; name-carrying ones read the registry populated
       when their key was built (in this session, by construction of raw). *)
    fun registry_constituents uk name =
      (case Universal_Key.constituents_of uk of
         SOME cs => cs
       | NONE => error ("build_entries: no constituents registered for " ^ name))
    fun mk_constituents (entity, uk, name) =
      case entity of
        Universal_Key.Theorem thm => #2 (Universal_Key.thm_constituents context thm)
      | Universal_Key.Introduction_Rule thm => #2 (Universal_Key.thm_constituents context thm)
      | Universal_Key.Elimination_Rule thm => #2 (Universal_Key.thm_constituents context thm)
      | Universal_Key.Induction_Rule thm => #2 (Universal_Key.thm_constituents context thm)
      | Universal_Key.Case_Split_Rule thm => #2 (Universal_Key.thm_constituents context thm)
      | Universal_Key.Named_Theorem _ => registry_constituents uk name
      | Universal_Key.Named_Introduction_Rule _ => registry_constituents uk name
      | Universal_Key.Named_Elimination_Rule _ => registry_constituents uk name
      | Universal_Key.Named_Induction_Rule _ => registry_constituents uk name
      | Universal_Key.Named_Case_Split_Rule _ => registry_constituents uk name
      | _ => []
  in
    (raw ~~ (provss ~~ prim_provenance)) |> map
      (fn ((entity, name, pos, uk, midx), (provs, prov)) =>
        let
          val hint =
            if is_thm_entity entity andalso not (null provs)
            then mk_instance_hint provs
                   (case prov of SOME (t, l, _) => (t, l) | NONE => (NONE, NONE))
            else mk_prompt_hint (entity, pos)
          (* A6: render coll(i) here (members carry bare coll + member_idx). *)
          val disp_name =
            case midx of
              SOME i => name ^ "(" ^ string_of_int i ^ ")"
            | NONE => name
        in
          (entity, disp_name, mk_prop_str (entity, pos),
           the_default 999999999 (Position.line_of pos), uk, hint, prov,
           mk_constituents (entity, uk, name))
        end)
  end

