(* --- Theorem cache: pre-computed non-infra theorems with universal keys --- *)

structure Thm_Cache = Theory_Data(struct
  (* `entries` = non-infra theorems (tag 0x02).  intro/elim/induct/case_ = the four
     per-kind rule buckets, each a SUBSET of `entries` re-tagged via
     Universal_Key.retag_thm_key (so a static rule fact is stored once as a theorem
     and once per kind it matches; uks differ only in byte 16).  All five share the
     `facts` snapshot whose complement is each callback's live delta. *)
  type T = {
    entries: Context_Callbacks.cached_thm_entry list,
    intro:  Context_Callbacks.cached_thm_entry list,
    elim:   Context_Callbacks.cached_thm_entry list,
    induct: Context_Callbacks.cached_thm_entry list,
    case_:  Context_Callbacks.cached_thm_entry list,
    facts: Facts.T
  }
  val empty = {entries = [], intro = [], elim = [], induct = [], case_ = [], facts = Facts.empty}
  fun merge ({entries = e1, intro = i1, elim = l1, induct = n1, case_ = c1, facts = f1},
             {entries = e2, intro = i2, elim = l2, induct = n2, case_ = c2, facts = f2}) =
    let
      (* dedup each bucket independently by #uk (a fresh table per bucket: the same
         thm's 0x02/0x12/... uks differ, so cross-bucket collision is impossible). *)
      fun dedup_pair (a, b) =
        let val seen = Bytehashtab.empty (length a + length b)
            fun add entry acc =
              if Bytehashtab.defined seen (#uk entry) then acc
              else (Bytehashtab.update seen (#uk entry, ()); entry :: acc)
        in fold add a (fold add b []) end
    in {entries = dedup_pair (e1, e2), intro = dedup_pair (i1, i2),
        elim = dedup_pair (l1, l2), induct = dedup_pair (n1, n2),
        case_ = dedup_pair (c1, c2), facts = Facts.merge (f1, f2)} end
end)

(* Process a list of (name, thm list) facts into cached_thm_entry list.
   Filters out concealed, hidden, and infra theorems; computes universal keys. *)
fun process_facts_into_cache (context : Context.generic)
    (is_infra_thm : string * thm -> bool)
    (facts_to_process : (string * thm list) list)
    : Context_Callbacks.cached_thm_entry list =
  let
    val thy = Context.theory_of context
    val all_facts = Context.cases Global_Theory.facts_of Proof_Context.facts_of context
    val fact_space = Facts.space_of all_facts
  in
    maps (fn (name, thms) =>
      let val entry_opt = try (Name_Space.the_entry fact_space) name
      in case entry_opt of
           NONE => []
         | SOME e =>
             if #concealed e orelse
                Long_Name.is_hidden (Name_Space.intern fact_space name)
             then []
             else let
               val epos = Context_Callbacks.entry_def_pos e
               val n = length thms
             in map_index (fn (i, thm) =>
               let val thm' = Thm.transfer thy thm
               in if is_infra_thm (name, thm') then NONE
                  else SOME {
                    uk = Universal_Key.key_of_theorem' NONE context thm',
                    (* fact-position name: bare for a singleton fact, name(i) for
                       a multi-thm fact member.  Same n=1->0 convention as the
                       collect path (get_theorems_with_positions), so the cached
                       QUERY name matches the collect name for multi-thm facts. *)
                    name = Context_Callbacks.Fixed (name, if n = 1 then 0 else i + 1),
                    pos = epos,
                    thm = thm',
                    theories = map #1 (#2 (Universal_Key.thm_constituents context thm'))
                  } : Context_Callbacks.cached_thm_entry option
               end) thms |> map_filter I
             end
      end) facts_to_process
  end

(* Classify already-built theorem cached_thm_entry list into the four rule buckets
   by O(1) retag of each entry's (tag-0x02) uk — no key recompute, no Facts rescan.
   This is the persistent counterpart of the old process_static_rule_facts scan.
   Sourcing from the is_infra_thm-pre-pruned theorem cache is SET-EQUIVALENT to the
   old direct scan ONLY because is_infra_induct_thm is a strict superset of
   is_infra_thm (infra_filter.ML): any fact infra-qua-theorem is also infra-qua-rule,
   so none is lost.  intro/elim inherit the is_infra_thm verdict already applied to
   the theorem cache; induct/case re-apply the stricter is_infra_induct_thm +
   has_rule_shape gate.  Name predicates are mutually exclusive (at most one fires),
   keyed on the BASE fact name (the Fixed constructor's name, NOT bare_name's
   rendered "name(i)") to match process_static_rule_facts, which keyed on the raw
   fact name.  WARNING: a future induct-specific whitelist exception to is_infra_thm
   would break the superset invariant and silently diverge here. *)
fun classify_rule_entries (is_infra_induct_thm : string * thm -> bool)
    (thm_entries : Context_Callbacks.cached_thm_entry list)
    : Context_Callbacks.cached_thm_entry list * Context_Callbacks.cached_thm_entry list
    * Context_Callbacks.cached_thm_entry list * Context_Callbacks.cached_thm_entry list =
  let
    fun retag (e : Context_Callbacks.cached_thm_entry) tag : Context_Callbacks.cached_thm_entry =
      {uk = Universal_Key.retag_thm_key (#uk e) tag,
       name = #name e, pos = #pos e, thm = #thm e, theories = #theories e}
    fun classify (e : Context_Callbacks.cached_thm_entry) =
      case #name e of
        Context_Callbacks.Fixed (base, _) =>
          if Theory_Structure.is_introduction_rule_name base then SOME (1, retag e 0wx12)
          else if Theory_Structure.is_elimination_rule_name base then SOME (2, retag e 0wx22)
          else if Theory_Structure.is_induction_rule_name base then
            (if is_infra_induct_thm (base, #thm e) orelse not (Theory_Structure.has_rule_shape (#thm e))
             then NONE else SOME (3, retag e 0wx32))
          else if Theory_Structure.is_case_split_rule_name base then
            (if is_infra_induct_thm (base, #thm e) orelse not (Theory_Structure.has_rule_shape (#thm e))
             then NONE else SOME (4, retag e 0wx42))
          else NONE
        (* Member_of_Dynamic entries never reach here (dynamic members get their own
           rule-kind entries via process_dynamic_facts_into_cache). *)
      | _ => NONE
    val rows = map_filter classify thm_entries
    fun pick t = map_filter (fn (tag, e) => if tag = t then SOME e else NONE) rows
  in (pick 1, pick 2, pick 3, pick 4) end

(* Enumerate the members of all visible non-infra dynamic collections
   (named_theorems + raw add_thms_dynamic collections such as
   derivative_eq_intros) as cached_thm_entry.  Members carry
   name = Member_of_Dynamic (collection full name) and NO position; identity is
   the content-addressed uk.  A plain single pass (runs once per AoA prep,
   inside make_entity_callbacks) — no internal cache. *)
fun process_dynamic_facts_into_cache (context : Context.generic)
    (is_infra_dynamic_fact : string -> bool)
    (is_infra_thm : string * thm -> bool)
    (is_infra_induct_thm : string * thm -> bool)
    (only_new : bool)
    : (Context_Callbacks.cached_thm_entry * int * Universal_Key.entity) list =
  let
    val thy = Context.theory_of context
    val facts = Context.cases Global_Theory.facts_of Proof_Context.facts_of context
    val space = Facts.space_of facts
    val coll_names =
      Name_Space.get_names space
      |> filter (Facts.is_dynamic facts)
      |> filter (not o is_infra_dynamic_fact)
    (* A2.0 only_new (collect): a member is "new in this theory" iff its content-
       addressed 0x02 uk is NOT among the SAME bin's members as seen from any
       parent theory's own context (Proof_Context.init_global p).  Because the uk
       is content-addressed (collector-independent), the parent re-eval yields the
       same uk for an inherited member, so current \ parent leaves exactly this
       theory's additions.  Done per bin (`parent_uks coll`), never as a global
       set.  query passes only_new = false, making parents = [] and parent_uks
       empty, so nothing is dropped (all reachable members surface). *)
    val parents = if only_new then Theory.parents_of thy else []
    fun parent_uks coll =
      let val tab = Bytehashtab.empty 256
      in List.app (fn p =>
           let val pctxt = Context.Proof (Proof_Context.init_global p)
               val pfacts = Global_Theory.facts_of p
           in case Context_Callbacks.dynamic_fact_members pctxt pfacts coll of
                NONE => ()
              | SOME pthms =>
                  List.app (fn pthm =>
                    Bytehashtab.update tab
                      (Universal_Key.key_of_theorem' NONE pctxt (Thm.transfer p pthm), ()))
                    pthms
           end) parents;
         tab
      end
    (* A2.1: a rule-suffixed bin yields, per member, the Theorem (0x02) entry AND
       a rule-kind entry.  The kind is fixed by the bin NAME (at most one fires).
       intro/elim use is_infra_thm; induct/case additionally require has_rule_shape
       on the member and use is_infra_induct_thm — mirroring the static path. *)
    fun rule_kind_of coll =
      if Theory_Structure.is_introduction_rule_name coll
        then SOME (Universal_Key.Introduction_Rule, Universal_Key.key_of_introduction_rule',
                   is_infra_thm, false)
      else if Theory_Structure.is_elimination_rule_name coll
        then SOME (Universal_Key.Elimination_Rule, Universal_Key.key_of_elimination_rule',
                   is_infra_thm, false)
      else if Theory_Structure.is_induction_rule_name coll
        then SOME (Universal_Key.Induction_Rule, Universal_Key.key_of_induction_rule',
                   is_infra_induct_thm, true)
      else if Theory_Structure.is_case_split_rule_name coll
        then SOME (Universal_Key.Case_Split_Rule, Universal_Key.key_of_case_split_rule',
                   is_infra_induct_thm, true)
      else NONE
  in
    (* Dedup members by uk, keeping the first occurrence (and its position):
       identical theorems within a collection, or a theorem shared by several
       collections, share a uk; keeping one avoids duplicate same-name entries
       reaching the interpretation answer endpoint (which matches by name). *)
    let val seen = Bytehashtab.empty 256 in
    maps (fn coll =>
      let val pset = parent_uks coll
          val rk = rule_kind_of coll in
      case Context_Callbacks.dynamic_fact_members context facts coll of
        NONE => []
      | SOME thms =>
          (* index j+1 = the member's 1-based position in Facts.lookup's list
             (numbered BEFORE infra filtering and BEFORE the only_new drop, so j
             stays the FULL-bin position — collect and query agree on coll(i)).
             It is NOT authoritative for citation: the query path recomputes the
             index live via `resolve_name`, and if the collection holds aconv-
             duplicate props the live `coll(i)` may point at the first such member
             rather than this exact one. That is recall-only, not a correctness
             hazard: a whole-collection / coll(i) reference is always re-resolved
             by Isabelle against the live collection content at use time. *)
          map_index (fn (j, thm) =>
            let val thm' = Thm.transfer thy thm
                val uk0 = Universal_Key.key_of_theorem' NONE context thm'
                val theories = map #1 (#2 (Universal_Key.thm_constituents context thm'))
                fun entry (entity, uk) =
                  ({uk = uk, name = Context_Callbacks.Member_of_Dynamic coll,
                    pos = ("", 0, 0), thm = thm', theories = theories}
                   : Context_Callbacks.cached_thm_entry, j + 1, entity)
            in
              if Bytehashtab.defined pset uk0 then []  (* only_new: inherited -> drop all kinds *)
              else
                (if is_infra_thm (coll, thm') then []
                 else [entry (Universal_Key.Theorem thm', uk0)])
                @ (case rk of
                     NONE => []
                   | SOME (con, key_of, infra, needs_shape) =>
                       if infra (coll, thm')
                          orelse (needs_shape andalso not (Theory_Structure.has_rule_shape thm'))
                       then []
                       else [entry (con thm', key_of NONE context thm')])
            end) thms
          |> flat
      end) coll_names
    |> filter (fn (e, _, _) =>
         if Bytehashtab.defined seen (#uk e) then false
         else (Bytehashtab.update seen (#uk e, ()); true))
    end
  end

(* Dedup new_entries against existing entries by universal key. *)
fun dedup_against existing new_entries =
  let
    val seen = Bytehashtab.empty (length existing)
    val _ = List.app (fn e => Bytehashtab.update seen (#uk e, ())) existing
  in filter (fn e => not (Bytehashtab.defined seen (#uk e))) new_entries end


(* Convenience callbacks with infra filters and theorem cache baked in.
   Processes any uncached facts at creation time so callbacks see 0 uncached. *)
fun make_entity_callbacks (init_context: Context.generic) (ctxt_unpack: Context_Callbacks.context_unpacker) =
  let (* Populate coercion-transparent matching ignore set (idempotent). *)
      val _ = List.app (fn n => Strhashtab.update Context_Callbacks.coercion_ignores (n, ())) [
        "Nat.semiring_1_class.of_nat",
        "Int.ring_1_class.of_int",
        "Rat.field_char_0_class.of_rat",
        "Real_Vector_Spaces.of_real",
        "Rings.zero_neq_one_class.of_bool",
        "Extended_Nat.enat",
        "Extended_Real.ereal.ereal",
        "Extended_Nonnegative_Real.ennreal"]
      val {is_infra_const, is_infra_thm, is_infra_induct_thm,
           is_infra_type, is_infra_class, is_infra_locale,
           is_infra_dynamic_fact, is_infra_method,
           is_declared_infra_thm, has_infra_decls} =
        Infra_Filter.gen_infra_filters init_context
      val {entries = cached_entries0, intro = cached_intro0, elim = cached_elim0,
           induct = cached_induct0, case_ = cached_case0, facts = cached_facts} =
        Thm_Cache.get (Context.theory_of init_context)
      (* Drop user-declared infra entries from the persistent caches.  The live KNN
         CACHED pass (context.ML make_thm_like_callback) does NOT re-apply the infra
         filter — it trusts entries were filtered at build time — so a declaration made
         after a fact was cached would otherwise still surface.  is_declared_infra_thm
         adds exactly the decl-driven drops (name match + const-mention cascade).  Gated
         on has_infra_decls, so without any `declare` this is a no-op and runs are
         byte-identical.  The delta/dynamic-member lists below are recomputed live this
         run through is_infra_thm/is_infra_induct_thm, which already see declarations. *)
      fun fact_base_name (Context_Callbacks.Fixed (nm, _)) = nm
        | fact_base_name (Context_Callbacks.Member_of_Dynamic coll) = coll
      fun drop_declared es =
        if has_infra_decls
        then filter (fn (e : Context_Callbacks.cached_thm_entry) =>
               not (is_declared_infra_thm (fact_base_name (#name e), #thm e))) es
        else es
      val cached_entries = drop_declared cached_entries0
      val cached_intro = drop_declared cached_intro0
      val cached_elim = drop_declared cached_elim0
      val cached_induct = drop_declared cached_induct0
      val cached_case = drop_declared cached_case0
      (* Process uncached facts now, so callbacks see 0 uncached.  Option A: the
         proof-local theorem delta is folded into BOTH the theorem cache and the
         four rule buckets (via classify_rule_entries), with snapshot = current_facts,
         so each callback's live delta is empty and proof-local rules surface via the
         CACHED pass with is_local = false — exactly as the old full-rescan path did. *)
      val current_facts = Context.cases Global_Theory.facts_of Proof_Context.facts_of init_context
      val uncached_facts = Facts.dest_static false [cached_facts] current_facts
      val delta_entries =
        if null uncached_facts then []
        else dedup_against cached_entries
               (process_facts_into_cache init_context is_infra_thm uncached_facts)
      val static_combined = cached_entries @ delta_entries
      val (delta_intro, delta_elim, delta_induct, delta_case) =
        classify_rule_entries is_infra_induct_thm delta_entries
      (* Dynamic-collection members: snapshot once here (never enter the persistent
         Thm_Cache).  process_dynamic emits, per member, a Theorem (0x02) entry AND a
         rule-kind entry; partition by the entity constructor so each goes to its own
         cache.  Q1 fix: ONLY Theorem-kind members enter the theorems cache (the rule
         entries used to pollute it via the old `map #1`). *)
      val dyn = process_dynamic_facts_into_cache init_context is_infra_dynamic_fact
                  is_infra_thm is_infra_induct_thm false
      fun members pred = map_filter (fn (cte, _, entity) =>
            if pred entity then SOME cte else NONE) dyn
      val theorem_members = members (fn Universal_Key.Theorem _ => true | _ => false)
      (* Bundle long-name -> its non-infra member thms, for the theorem-collection
         pattern-match gate (context.ML make_theorem_collection_callback).  Built
         per-collection via dynamic_fact_members, NOT from the globally-uk-deduped `dyn`:
         `dyn`'s single seen-table drops a member shared by two bundles from whichever
         enumerates second, which would skew that bundle's match-count and make the
         keep/drop decision order-dependent.  Wrapped in Lazy: the build (one
         dynamic_fact_members sweep) is deferred and memoized, so it runs at most once
         per `by aoa` and ONLY if a patterned bundle query forces it — an unpatterned
         proof pays nothing.  Keys are the same long-names Name_Space.get_names yields
         for the dynamic-fact space, matching what ns_filter passes to the callback's
         extra_filter.  A bundle with no non-infra members is omitted (lookup miss =>
         not shown under patterns, per the zero-members rule). *)
      val collection_member_thms : thm list Strhashtab.table Lazy.lazy =
        Lazy.lazy (fn () =>
          let val thy = Context.theory_of init_context
              val space = Facts.space_of current_facts
              val tab = Strhashtab.empty 64
              val _ = List.app (fn coll =>
                        if not (Facts.is_dynamic current_facts coll)
                           orelse is_infra_dynamic_fact coll then ()
                        else case Context_Callbacks.dynamic_fact_members
                                    init_context current_facts coll of
                               NONE => ()
                             | SOME thms =>
                                 let val members = thms
                                       |> map (Thm.transfer thy)
                                       |> filter (fn thm => not (is_infra_thm (coll, thm)))
                                 in if null members then ()
                                    else Strhashtab.update tab (coll, members) end)
                      (Name_Space.get_names space)
          in tab end)
      val intro_members  = members (fn Universal_Key.Introduction_Rule _ => true | _ => false)
      val elim_members   = members (fn Universal_Key.Elimination_Rule _ => true | _ => false)
      val induct_members = members (fn Universal_Key.Induction_Rule _ => true | _ => false)
      val case_members   = members (fn Universal_Key.Case_Split_Rule _ => true | _ => false)
      (* Theorem members deduped against the static set so a member that is also a
         static fact keeps the static (Fixed) name/position. *)
      val combined_entries = static_combined @ dedup_against static_combined theorem_members
      val thm_cache = SOME (combined_entries, current_facts)
      (* Per-kind rule caches: persistent buckets (Thm_Cache, built incrementally by
         classify_rule_entries in update_thm_cache) + this call's proof-local delta +
         dynamic members.  Persistent/delta first so a member that is also a static
         rule fact keeps the static (Fixed) name.  current_facts is the snapshot
         pinning each rule callback's live Source-2 delta (empty under Option A). *)
      val intro_static  = cached_intro  @ delta_intro
      val elim_static   = cached_elim   @ delta_elim
      val induct_static = cached_induct @ delta_induct
      val case_static   = cached_case   @ delta_case
      fun rule_cache static mems = SOME (static @ dedup_against static mems, current_facts)
  in {
    constants = Context_Callbacks.make_constants_callback (SOME (not o is_infra_const)) ctxt_unpack,
    theorems = Context_Callbacks.make_theorems_callback (SOME (not o is_infra_thm)) thm_cache ctxt_unpack,
    types = Context_Callbacks.make_types_callback (SOME (not o is_infra_type)) ctxt_unpack,
    classes = Context_Callbacks.make_classes_callback (SOME (not o is_infra_class)) ctxt_unpack,
    locales = Context_Callbacks.make_locales_callback (SOME (not o is_infra_locale)) ctxt_unpack,
    named_theorems = Context_Callbacks.make_theorem_collection_callback
      collection_member_thms (SOME (not o is_infra_dynamic_fact)) ctxt_unpack,
    methods = Context_Callbacks.make_methods_callback (SOME (not o is_infra_method)) ctxt_unpack,
    introduction_rules = Context_Callbacks.make_introduction_rules_callback
      extract_intro_rules Theory_Structure.is_introduction_rule_name (K true)
      (SOME (not o is_infra_thm)) (rule_cache intro_static intro_members) ctxt_unpack,
    elimination_rules = Context_Callbacks.make_elimination_rules_callback
      extract_elim_rules Theory_Structure.is_elimination_rule_name (K true)
      (SOME (not o is_infra_thm)) (rule_cache elim_static elim_members) ctxt_unpack,
    induction_rules = Context_Callbacks.make_induction_rules_callback
      extract_induct_net Theory_Structure.is_induction_rule_name Theory_Structure.has_rule_shape
      (SOME (not o is_infra_induct_thm)) (rule_cache induct_static induct_members) ctxt_unpack,
    case_split_rules = Context_Callbacks.make_case_split_rules_callback
      extract_case_split_net Theory_Structure.is_case_split_rule_name Theory_Structure.has_rule_shape
      (SOME (not o is_infra_thm)) (rule_cache case_static case_members) ctxt_unpack
  } end

fun interpret' (context : Context.generic) : usage * usage =
  let
    (* 1. Get theory information *)
    val thy = Context.theory_of context
    val thy_name = Context.theory_long_name thy
    val thy_file_path = Theory_Structure.get_theory_file_path thy
    (* 2. Extract constants and theorems, filtering infrastructure *)
    val {is_infra_const, is_infra_thm, is_infra_induct_thm, is_infra_type, is_infra_class,
         is_infra_locale, is_infra_dynamic_fact, is_infra_method, ...} =
      Infra_Filter.gen_infra_filters context

    val const_entries = Theory_Structure.get_constants_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_const name then NONE
          else let val entity = Universal_Key.Constant name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val raw_thm_entries = Theory_Structure.get_theorems_with_positions context
      |> filter (fn (name, _, _, thm, _) => not (is_infra_thm (name, thm)))

    (* 2c. Build named entries and check which appear in the source file. *)
    val raw_thm_entries_named = raw_thm_entries
      |> map (fn (name, idx, pos, thm, _) =>
        (Thm_Name.print (name, idx), pos, thm))
    val thy_prefix = thy_name ^ "."
    fun strip_thy_prefix name =
      if String.isPrefix thy_prefix name
      then String.extract (name, size thy_prefix, NONE)
      else name
    val file_match_names = map (strip_thy_prefix o #1) raw_thm_entries_named
    val name_check_results = Context.cases
      (fn _ => Remote_Procedure_Calling.call_command
          check_theorem_name_in_file_cmd (thy_file_path, file_match_names)
        handle Remote_Procedure_Calling.Remote_Calling_Failure {message, ...} =>
          (warning ("check_theorem_name_in_file RPC failed: " ^ message);
           map (K (0, ~1)) file_match_names))
      (fn _ => map (K (0, ~1)) file_match_names)
      context
    val raw_thm_entries_with_offsets =
          (raw_thm_entries_named ~~ name_check_results)

    (* 3. Compute universal keys for theorems *)
    val thm_entries_with_uks = raw_thm_entries_with_offsets
      |> map (fn ((name, pos, thm), (word_off, goal_off)) =>
        let val uk = Universal_Key.key_of NONE context (Universal_Key.Theorem thm)
        in (uk, name, pos, thm, word_off, goal_off) end)

    (* 4. Deduplicate theorems by universal key *)
    val best_thms : (string * Position.T * thm * int * int) Thm_Dedup.table =
      Thm_Dedup.empty 1024
    val _ = thm_entries_with_uks
      |> List.app (fn (uk, name, pos, thm, word_off, goal_off) =>
        case Thm_Dedup.lookup best_thms uk of
          NONE => Thm_Dedup.update best_thms
            (uk, (name, pos, thm, word_off, goal_off))
        | SOME (_, _, _, prev_word_off, prev_goal_off) =>
            let val dominated =
              if goal_off >= 0 andalso prev_goal_off < 0 then true
              else if prev_goal_off >= 0 andalso goal_off < 0 then false
              else if goal_off >= 0 then goal_off < prev_goal_off
              else word_off < prev_word_off
            in if dominated
               then Thm_Dedup.update best_thms
                 (uk, (name, pos, thm, word_off, goal_off))
               else ()
            end)
    val deduped_thm_entries = Thm_Dedup.fold
      (fn (uk, (name, pos, thm, _, _)) => fn acc =>
        (Universal_Key.Theorem thm, name, pos, uk) :: acc)
      best_thms []

    (* 5a''. Dynamic-collection members as Theorem entities, deduped against the
       static theorems by uk (static name/pos wins).  A6: each member carries the
       BARE collection name + member_idx = SOME i (the carried full-bin position
       from process_dynamic).  build_entries renders coll(i) at the record-write
       from member_idx; the locale-provenance bypass stays keyed on
       Facts.is_dynamic of the (bare) name (covers both members and the
       Theorem_Collection entity, which a member_idx-only test would miss).
       No member_pos / post-pass: the global keep-first below resolves
       member-vs-static by uk (static, with a real line, wins; a dropped member
       takes its SOME i with it, so no surviving static entry is renamed). *)
    val member_entries =
      let
        val seen = Bytehashtab.empty 256
        val _ = List.app (fn (_, _, _, uk) => Bytehashtab.update seen (uk, ()))
                  deduped_thm_entries
      in
        process_dynamic_facts_into_cache context is_infra_dynamic_fact is_infra_thm
          is_infra_induct_thm true
        |> map_filter (fn (e, i, entity) =>
             (* `entity` is the per-kind entity (Theorem 0x02 or a rule kind);
                its #uk matches.  Dedup only against static THEOREMS here (seen
                holds 0x02 uks); a rule member equal to a static rule is resolved
                later by the global keep-first (static wins). *)
             if Bytehashtab.defined seen (#uk e) then NONE
             else SOME (entity,
                        Context_Callbacks.bare_name (#name e),
                        Position.none, #uk e, SOME i))
      end

    (* 5. Extract types, classes, and locales *)
    val type_entries = Theory_Structure.get_types_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_type name then NONE
          else let val entity = Universal_Key.Type name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val class_entries = Theory_Structure.get_classes_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_class name then NONE
          else let val entity = Universal_Key.Class name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val locale_entries = Theory_Structure.get_locales_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_locale name then NONE
          else let val entity = Universal_Key.Locale name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    (* 5a'. Extract named_theorems collections and proof methods *)
    val named_theorems_entries = Theory_Structure.get_theorem_collections_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_dynamic_fact name then NONE
          else let val entity = Universal_Key.Theorem_Collection name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val method_entries = Theory_Structure.get_methods_with_positions context
      |> map_filter (fn (name, pos) =>
          if is_infra_method name then NONE
          else let val entity = Universal_Key.Method name
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    (* 5b. Extract introduction and elimination rules *)
    val intro_entries = Theory_Structure.get_introduction_rules_with_positions context
      |> map_filter (fn (name, pos, thm) =>
          if is_infra_thm (#1 (Thm_Name.parse name), thm) then NONE  (* bare name: name(i) would trip is_hidden *)
          else let val entity = Universal_Key.Introduction_Rule thm
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val elim_entries = Theory_Structure.get_elimination_rules_with_positions context
      |> map_filter (fn (name, pos, thm) =>
          if is_infra_thm (#1 (Thm_Name.parse name), thm) then NONE
          else let val entity = Universal_Key.Elimination_Rule thm
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    (* 5c. Extract induction and case-split rules *)
    val induct_entries = Theory_Structure.get_induction_rules_with_positions context
      |> map_filter (fn (name, pos, thm) =>
          if is_infra_thm (#1 (Thm_Name.parse name), thm) then NONE
          else let val entity = Universal_Key.Induction_Rule thm
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    val case_split_entries = Theory_Structure.get_case_split_rules_with_positions context
      |> map_filter (fn (name, pos, thm) =>
          if is_infra_thm (#1 (Thm_Name.parse name), thm) then NONE
          else let val entity = Universal_Key.Case_Split_Rule thm
               in SOME (entity, name, pos,
                    Universal_Key.key_of NONE context entity) end)

    (* 6. Build entries list, sorted by source position, then a single global
       uk-dedup (reusing the uk-keyed Bytehashtab) so EVERY entry that reaches
       build_entries / the agent has a unique universal_key.

       The theorem list (deduped_thm_entries, step 4 via Thm_Dedup with
       source-position dominance) and the dynamic members (member_entries, step
       5a'', deduped against theorems + internally) are already uk-unique.  The
       ONLY remaining hole is the four rule lists (intro/elim/induct/case-split),
       which were previously concatenated with NO uk dedup: two differently
       named but content-equal rules in one list (same kind-tag, same thm128 =>
       same uk) would both reach the agent — wasted interpretation + last-write-
       wins LMDB overwrite.  Rules never collide with theorem/member by uk
       (distinct kind-tag byte in the key), so this keep-first pass removes only
       intra-rule-list duplicates (keeping the position-dominant one, since the
       list is already sorted by entry_ord) and leaves every other kind
       untouched.  Done on the flat (entity,name,pos,uk,member_idx) tuples BEFORE
       build_entries so the positional raw ~~ provss zip there stays aligned.
       Non-member producers carry member_idx = NONE; member_entries already carry
       SOME i.  A member whose uk equals a static entry's is dropped here (static,
       with a real line, sorts first), taking its SOME i with it — so no surviving
       entry is ever mis-rendered as coll(i). *)
    val all_entries_with_pos =
      let val tag = map (fn (e, n, p, u) => (e, n, p, u, NONE)) in
        (tag (const_entries @ deduped_thm_entries)
         @ member_entries
         @ tag (type_entries @ class_entries @ locale_entries
                @ named_theorems_entries @ method_entries
                @ intro_entries @ elim_entries
                @ induct_entries @ case_split_entries))
        |> sort entry_ord
        |> (fn es =>
              let val seen = Bytehashtab.empty (Int.max (16, length es))
              in filter (fn (_, _, _, uk, _) =>
                   if Bytehashtab.defined seen uk then false
                   else (Bytehashtab.update seen (uk, ()); true)) es
              end)
      end
    (* coll(i) is rendered inside build_entries (from member_idx); no post-pass. *)
    val entries = build_entries context all_entries_with_pos
    (* Enforce (entity-kind, name) uniqueness over the FINAL entries (with each
       member's coll(i) already rendered by build_entries), before they reach the
       agent.  The Python interpreter
       keys its results dict / answer routing on f"{KIND_TITLE} {name}", and
       KIND_TITLE is a function of Universal_Key.entity_kind_int (several
       constructors collapse to one title); two entries sharing
       (entity_kind_int, name) would silently collapse there and mis-route
       translations onto the wrong universal_key.  This converts the chain of
       implicit label-uniqueness arguments — Name_Space uniqueness per kind;
       theorem (name,idx) dedup; member coll(i); and the Static/Dynamic fact
       partition (facts.ML) keeping a member's coll(i) name disjoint from a
       static name(idx) of the same kind — into ONE checked invariant.  It
       should never fire unless a future change reintroduces a duplicate label.
       NB: the second (currently dead) entry point interpret_entities does not
       run this check; if it is revived with rule/member entities, share it.
       Scope: this check deliberately ranges over the FULL final entries (before
       the cached/uncached split), so it is STRICTER than the Python-side
       label-uniqueness guard (semantic_interpretation.py), which sees only the
       uncached subset actually sent to the agent.  The asymmetry is intentional
       fail-closed defence-in-depth: ML aborts the whole run on any duplicate,
       even one confined to already-cached entries, rather than letting it reach
       the interpreter. *)
    val _ =
      let
        fun uk_hex uk =
          Word8Vector.foldr
            (fn (b, acc) => (if b < 0w16 then "0" else "") ^ Word8.toString b ^ acc) "" uk
        val seen : Universal_Key.universal_key Strhashtab.table =
          Strhashtab.empty (Int.max (16, length entries))
      in
        List.app (fn (ent, nm, _, _, uk, _, _, _) =>
          let val label = Int.toString (Universal_Key.entity_kind_int ent) ^ " " ^ nm
          in case Strhashtab.lookup seen label of
               SOME uk0 =>
                 error (String.concat
                   ["Semantic_Store.interpret': duplicate interpretation label ",
                    quote label, " — two distinct entities map to one agent-facing ",
                    "(kind,name) label and would collapse silently in the interpreter ",
                    "(entity-kind int ", Int.toString (Universal_Key.entity_kind_int ent),
                    ", name ", quote nm, "; uks ", uk_hex uk0, " vs ", uk_hex uk, ")"])
             | NONE => Strhashtab.update seen (label, uk)
          end) entries
      end
  in
    if null entries then (zero_usage, zero_usage)
    else let
      (* 7. Build callbacks capturing context *)
      val su = Context_Callbacks.static_context_unpacker context
      val pu = PIDE_State.position_context_unpacker context
      val uk_cb = Universal_Key.make_universal_key_callback pu
      val theory_info_cb = Context_Callbacks.make_theory_long_name_and_path_callback su
      val desugar_cb = Explain_Term.make_desugar_callback context
      val resolve_notation_cb = Explain_Term.make_resolve_notation_callback context
      (* pu, not su: the query tool's show_defs threads a (file, offset)
         context through entities_of into these callbacks; the static
         unpacker accepts only nil and would fail to decode it. *)
      val {constants, theorems, types, classes, locales, named_theorems, methods,
           introduction_rules, elimination_rules,
           induction_rules, case_split_rules} = make_entity_callbacks context pu
      val cmd = make_interpret_file_cmd [uk_cb, theory_info_cb, desugar_cb,
            resolve_notation_cb,
            constants, theorems, types, classes, locales, named_theorems, methods,
            introduction_rules, elimination_rules,
            induction_rules, case_split_rules]

      (* 8. Call RPC command *)
      val thy_key = Universal_Key.key_of_theory' thy
      val rpc_arg = {
        file_path = thy_file_path,
        theory_longname = thy_name,
        theory_key = thy_key,
        entries = entries
      }
      val (_, _, current, cumulative) =
        Remote_Procedure_Calling.call_command cmd rpc_arg
          handle Remote_Procedure_Calling.Remote_Calling_Failure {message, ...} =>
            error (String.concat ["Semantic interpretation RPC failed: ", message])
      val _ = tracing (String.concat [
        "[Semantic_Store] ", thy_name, ": ", fmt_usage current,
        " (historical: ", fmt_usage cumulative, ")"])
    in
      (current, cumulative)
    end
  end

fun collect_uninterpreted (force : bool) thy =
  let
    val seen = Inthashtab.empty 64
    val _ = List.app (Inthashtab.update seen o rpair ()) base_theory_ids
    fun collect thy acc =
      let val id = Context.theory_identifier thy
      in
        if Inthashtab.defined seen id orelse is_infra_session thy
        then acc
        else
          let val _ = Inthashtab.update seen (id, ())
              (* Always descend into ancestors, even past an already-interpreted
                 theory, so an uninterpreted ancestor is never skipped (we no
                 longer assume "interpreted" implies all ancestors interpreted).
                 The interpreted theory itself is still not re-collected. *)
              val acc' = fold collect (Theory.parents_of thy) acc
          in if not force andalso is_interpreted thy then acc' else acc' @ [thy] end
      end
  in collect thy [] end

(* Scheduler state for DAG-parallel interpretation (one Isabelle session).
   Default width N = Multithreading.max_threads (), optionally overridden by
   callers such as semantics_manage.py.
   blocking interpret' runs on off-pool Isabelle_Thread workers, concurrency bounded
   by `running < n`, a theory is admitted only after all its in-set ancestors are
   `done` (so its agent can read their interpretations from LMDB). Failure policy is
   HARD CRASH: the first error sets `failed`, all siblings are interrupted, and the
   exception is reraised out of `interpret` (the pipeline is resume-by-design). *)
type sched_state =
  {running: int, completed: int, done: Intset.T,
   failed: exn option, total: usage, hist: usage}

fun interpret_parallel (n : int) (theories : theory list) : usage * usage =
  let
    val collected_ids = Intset.make (map Context.theory_identifier theories)
    (* deps = transitive ancestors restricted to the collected set, so a skipped /
       already-interpreted intermediate never drops a real prerequisite edge. *)
    val nodes = map (fn thy =>
      {id = Context.theory_identifier thy,
       thy = thy,
       deps =
         Theory.ancestors_of thy
         |> map Context.theory_identifier
         |> filter (Intset.member collected_ids)
         |> Intset.make}) theories
    val total_count = length nodes

    val st = Synchronized.var "Semantic_Store.interpret_sched"
      ({running = 0, completed = 0, done = Intset.empty, failed = NONE,
        total = zero_usage, hist = zero_usage} : sched_state)

    fun admit {id = _, thy = _, deps} =
      Synchronized.guarded_access st
        (fn (s as {running, completed, done, failed, total, hist}) =>
          if is_some failed then SOME (false, s)
          else if running < n andalso Intset.subset (deps, done)
          then SOME (true, {running = running + 1, completed = completed,
                            done = done, failed = failed, total = total, hist = hist})
          else NONE)

    fun finish id (cur, cum) =
      Synchronized.change st
        (fn {running, completed, done, failed, total, hist} =>
          {running = running - 1, completed = completed + 1,
           done = Intset.insert id done, failed = failed,
           total = add_usage (total, cur), hist = add_usage (hist, cum)})

    fun set_failed exn =
      Synchronized.change st
        (fn {running, completed, done, failed, total, hist} =>
          {running = running, completed = completed, done = done,
           failed = SOME (the_default exn failed), total = total, hist = hist})

    (* `\<^try> ... catch` re-raises interrupts and only routes genuine errors to
       set_failed; bare `handle exn =>` would be a compile error here. *)
    fun worker (node as {id, thy, deps = _}) () =
      \<^try>\<open>
        if admit node
        then
          let val (cur, cum) = interpret' (Context.Theory thy)
              val _ = mark_interpreted thy
          in finish id (cur, cum) end
        else ()  (* failure already recorded: skip cleanly, no deadlock *)
      catch exn => set_failed exn\<close>

    val params =
      Isabelle_Thread.interrupts (Isabelle_Thread.params "Semantic_Store.interpret")
    val handles = map (fn node => Isabelle_Thread.fork params (worker node)) nodes

    (* Wait until a failure occurs or all theories have finished. *)
    val _ = Synchronized.guarded_access st
      (fn (s as {failed, completed, running = _, done = _, total = _, hist = _}) =>
        if is_some failed orelse completed = total_count
        then SOME ((), s) else NONE)

    val {failed, total, hist, running = _, completed = _, done = _} =
      Synchronized.value st
    val _ = if is_some failed
            then List.app Isabelle_Thread.interrupt_thread handles else ()
    val _ = List.app Isabelle_Thread.join handles
    val _ = case failed of SOME e => Exn.reraise e | NONE => ()
  in (total, hist) end

fun interpret_with_parallel parallel (force : bool) (context : Context.generic) : unit =
  let
    val thy = Context.theory_of context
    val theories = collect_uninterpreted force thy
    val contexts = map Context.Theory theories
      @ (case context of Context.Proof _ => [context] | _ => [])
    val _ = if null contexts then () else
      tracing ("[Semantic_Store] interpreting " ^
               Int.toString (length contexts) ^ " theories")

    val n =
      (case parallel of
        NONE => Multithreading.max_threads ()
      | SOME n =>
          if n >= 1 then n
          else error "Semantic_Store.interpret: parallel worker count must be positive")
    val (total, hist) =
      if n <= 1
      then
        fold (fn ctx => fn (acc, hacc) =>
          let val (cur, cum) = interpret' ctx
              val _ = Context.cases mark_interpreted (K ()) ctx
          in (add_usage (acc, cur), add_usage (hacc, cum)) end)
        contexts (zero_usage, zero_usage)
      else
        let
          val (ttotal, thist) = interpret_parallel n theories
          (* A trailing Proof context (if any) runs last, sequentially; like the
             sequential path it is interpreted but not marked. *)
          val (ptotal, phist) =
            case context of
              Context.Proof _ => interpret' context
            | _ => (zero_usage, zero_usage)
        in (add_usage (ttotal, ptotal), add_usage (thist, phist)) end

    val _ = tracing (String.concat [
        "[Semantic_Store] total: ", fmt_usage total,
        " (historical: ", fmt_usage hist, ")"])
  in
    ()
  end

fun interpret force context = interpret_with_parallel NONE force context

fun mk_interpret_theory (context : Context.generic) (names : string list) : unit =
  let
    val theories = map (Theory_Hash.resolve_theory context) names
    val uninterpreted = filter (not o is_interpreted) theories
    val contexts = map Context.Theory uninterpreted
    val _ = if null contexts then () else
      tracing ("[Semantic_Store] mk_interpret_theory: interpreting " ^
               Int.toString (length contexts) ^ " theories")
  in
    List.app (fn ctx =>
      let val _ = interpret' ctx
          val _ = Context.cases mark_interpreted (K ()) ctx
      in () end)
    contexts
  end

fun make_interpret_theory_callback (ctxt_unpack : Context_Callbacks.context_unpacker)
    : Remote_Procedure_Calling.callback' =
  Remote_Procedure_Calling.mk_callback {
